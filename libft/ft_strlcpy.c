#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t destsize)
{
	size_t	srcsize;

	srcsize = ft_strlen(src);
	if (destsize > srcsize + 1)
		ft_memcpy(dest, src, srcsize + 1);
	else if (destsize != 0)
	{
		ft_memcpy(dest, src, destsize - 1);
		dest[destsize - 1] = 0;
	}
	return (srcsize);
}
